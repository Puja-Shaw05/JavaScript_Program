// Print number from 1 to 10
console.log("1 to 10 numbers");
for(let i=1; i<=10; i++){
  console.log(i);
}

// Print Even number from 1 to 10
console.log("1 to 10 Even numbers");
for(let i=1; i<=10; i++){
  if( i % 2 == 0)
  console.log(i);
}

// Print Odd number from 1 to 10
console.log("1 to 10 Odd numbers");
for(let i=1; i<=10; i++){
  if( i % 2 != 0)
  console.log(i);
}
